# --- Gradient Boosting challenger ---
# THE key honesty caveat of this whole module: there is no real repayment/
# default outcome anywhere in this synthetic dataset (there are no loans
# that have actually been repaid or defaulted over time - Module 1
# generates a snapshot, not a loan-performance history). The hidden
# true_archetype label (healthy/stagnant/distressed) - generated by
# Module 1 to drive the synthetic data's internal consistency, and
# otherwise used ONLY for backtesting the rule-based scorecard's ordering
# in module3/module5's validate.py - is repurposed here as the closest
# available STAND-IN for a real repayment-outcome label, exactly as the
# deck's architecture answer already promised ("retrained via ... real
# repayment outcomes" - this is that slot, filled with the best proxy a
# synthetic dataset can offer). TARGET_BY_ARCHETYPE below is NOT a
# calibrated probability of default - it's an illustrative anchor so the
# regressor has a continuous target to fit, chosen to roughly track
# GRADE_BANDS' band centers. Swap this file's target-construction step for
# real observed repayment outcomes the day real loan performance data
# exists; nothing else in this module needs to change.
TARGET_BY_ARCHETYPE = {
    "healthy": 85.0,
    "stagnant": 55.0,
    "distressed": 20.0,
}

GRADIENT_BOOSTING_PARAMS = {
    "max_iter": 150,
    "max_depth": 4,
    "learning_rate": 0.08,
    "random_state": 42,
}

TRAIN_TEST_SPLIT_FRAC = 0.7
SPLIT_RANDOM_STATE = 42

# Verified by direct inspection (grouping borrower_features.csv by hidden
# true_archetype): projected_revenue_growth_rate has ZERO overlap between
# archetype groups in this synthetic dataset (distressed max -0.070,
# stagnant range 0.009-0.015, healthy min 0.096 - clean ~8-point gaps on
# both sides). Every other feature has realistic archetype overlap. This
# is a Module 1 data-generation artifact (that feature's archetype-
# conditioned noise is far tighter than the others'), not a Module 9 bug -
# it makes the full-feature-set challenger hit an implausible R2=1.0 /
# MAE=0.0 on held-out data, which would never happen on real data. Rather
# than quietly drop the feature from the deployed model (it's still a
# legitimately informative feature, same as any other), this second
# feature list is used ONLY for a secondary "what does the model learn
# from everything else" robustness/explainability run - see
# challenger_model.evaluate_holdout's docstring.
ROBUSTNESS_CHECK_FEATURE_COLUMNS = [c for c in ML_FEATURE_COLUMNS if c != "projected_revenue_growth_rate"]

# A borrower's champion (Module 5 composite_score) and challenger
# (Gradient Boosting) scores are flagged for manual review when they
# diverge by more than this many points on the shared 0-100 scale -
# exactly the "monitor divergence, don't auto-resolve it" behavior a real
# champion-challenger deployment uses.
#
# Recalibrated 15.0 -> 25.0 (user decision): at 15 points, 131/400 (33%)
# of borrowers were flagged - far too noisy for the flag to command any
# attention when it appears. At 25, ~35/400 (~9%) are flagged. Still an
# assumption, not calibrated against real outcomes (none exist in this
# synthetic dataset) - a production deployment recalibrates this against
# observed repayment data.
DIVERGENCE_FLAG_THRESHOLD = 25.0
