# score which is categorical-derived there - estimated_value_inr is used
# here instead as collateral's numeric signal). Both ML models see exactly
# this matrix - no feature the rule-based scorecard doesn't also see, so
# any divergence is a genuine "different model, same inputs" signal, not
# an artifact of the challenger having access to more data.
