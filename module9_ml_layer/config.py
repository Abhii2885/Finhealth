"""
Module 9 - ML Layer: Champion-Challenger (Track 3 add-on)

The deck's architecture describes a champion-challenger pattern: the
rule-based 5C scorecard (Modules 4-5) stays the CHAMPION - the score of
record, the one a credit manager actually acts on - while two ML models
run in parallel as CHALLENGERS, purely for monitoring and future
validation, never silently substituted in:

- Isolation Forest: unsupervised multivariate anomaly detection over the
  same submetric feature matrix Module 5 scores. Flags borrowers whose
  overall feature PROFILE is statistically unusual for this population,
  independent of whether their score is good or bad - a borrower can be
  perfectly healthy and still be an outlier (e.g. unusually large
  collateral value relative to turnover), which is exactly the kind of
  signal a rule-based scorecard structurally cannot surface, since it
  only ever looks at one submetric at a time.
- Gradient Boosting (HistGradientBoostingRegressor): a supervised
  challenger trained to predict a risk-proxy target from Module 3's raw
  features, independent of the champion's own weights/thresholds - see
  TARGET_BY_ARCHETYPE below for the one honesty caveat that matters most
  in this module.

Read the README before trusting any number this module produces.
"""

import os

DEFAULT_FEATURES_DIR = os.path.join(os.path.dirname(__file__), "..", "module3_feature_engineering", "features_output")
DEFAULT_SCORING_DIR = os.path.join(os.path.dirname(__file__), "..", "module5_scoring", "scoring_output")
DEFAULT_DATA_LAKE_DIR = os.path.join(os.path.dirname(__file__), "..", "module1_data_ingestion", "data_lake")
OUTPUT_DIR = os.path.join(os.path.dirname(__file__), "ml_output")

# The 22 raw numeric submetrics Module 5 already scores (same set as
